// Function to generate metadata for a specific page and locale
export function generatePageMetadata(
  pageKey,
  locale = "ar",
  siteUrl = "https://yoursite.com",
) {
  const pageData = seoData[locale]?.[pageKey] || seoData.en[pageKey];

  return {
    title: pageData.title,
    description: pageData.description,
    keywords: pageData.keywords,
    robots: "index, follow",
    openGraph: {
      type: "website",
      locale: locale === "ar" ? "ar_SA" : "en_US",
      url: `${siteUrl}/${locale}`,
      title: pageData.ogTitle,
      description: pageData.ogDescription,
      image: {
        url: `${siteUrl}/og-image-${pageKey}.png`,
        width: 1200,
        height: 630,
        alt: pageData.ogTitle,
      },
      siteName: "Start with a Question",
    },
    twitter: {
      card: "summary_large_image",
      title: pageData.ogTitle,
      description: pageData.ogDescription,
      image: `${siteUrl}/og-image-${pageKey}.png`,
      creator: "@yourhandle",
    },
  };
}

